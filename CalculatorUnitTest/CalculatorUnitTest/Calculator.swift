import UIKit

/// 簡単な計算を行うクラス
class Calculator {
    
    //2つの整数を加算するメソッド
    func add(_ a: Int, _ b: Int) -> Int {
        return a + b
    }
    
    //2つの整数を減算するメソッド
    func subtract(_ a: Int, _ b: Int) -> Int {
        return a - b
    }
}
